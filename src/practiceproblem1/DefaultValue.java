package practiceproblem1;

public class DefaultValue {
    byte a;
    short b;
    int c;
    long d;
    float e;
    double f;
    char  g;
    boolean b1;

    public static void main(String[] args) {
        DefaultValue obj1 = new DefaultValue();


        System.out.println("byte : "+ obj1.a);
        System.out.println("short :"+ obj1.b);
        System.out.println("int :"+ obj1.c);
        System.out.println("long :"+ obj1.d);
        System.out.println("float :"+ obj1.e);
        System.out.println("double :"+ obj1.f);
        System.out.println("char :"+ obj1.g);
        System.out.println(" boolean :"+ obj1.b1);

    }

}

